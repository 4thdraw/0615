# +-----------------+---------------------+---------------------+---------------------+-------------------------------------------------------+
# | object / str    | 문자열 / 텍스트     | "1월", "A등급"      | 텍스트 / 문자       | 판다스에서 일반적인 문자는 대부분 object로 표현됨     |
