# +-----------------+---------------------+---------------------+---------------------+-------------------------------------------------------+
# | datetime64[ns]  | 날짜와 시간         | 2026-06-15          | 날짜 / 시각         | 이 타입이어야 월별 합계, 연도별 추출 등 연산 가능     |
