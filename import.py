# 샘플 데이터 (정수기 공장 판매 기록)
데이터 = {
    "월": ["1월", "2월", "3월", "4월", "5월"],
    "싼거": [50, 65, 45, 70, 55],
    "기본": [80, 85, 90, 105, 110],
    "비싼거": [20, 35, 40, 60, 85]
}


# 2. 판다스 공장에 넣어서 표(DataFrame)로 조립하기
# 이 단계에서 판다스가 데이터를 정렬하여 우리가 아는 2차원 표로 만듭니다.
df = pd.DataFrame(데이터)

# 3. 콘솔창에 표 형태로 예쁘게 출력하기
print("=== 판다스가 조립한 최종 데이터프레임 표 ===")
print(df)

# 상위 3개만 보기
# print(df.head(3))

# 데이터프레임의 전체 요약 정보 출력
# df.info()

# '1월' 행의 데이터만 이름으로 가져오기 (행 인덱스가 번호로 되어 있다면 df.loc[0])
print(f" 첫 줄노출 {df.loc[0]}") 


print(f"첫줄 2번째 칸 출력 {df.iloc[0, 1]}") # 출력: 50


print(f" 2월 데이터행 노출 \n \n {df.loc[1]}") 

print(f"싼거 데이터열 노출\n{df['싼거']}")

